# Deklarasi variabel dengan nilai numerik
umur = 25
berat_badan = 65.5

# Deklarasi variabel dengan nilai string
nama = "John Doe"
alamat = '123 Main Street'

# Menampilkan nilai variabel ke layar
print("Nama:", nama)
print("Umur:", umur)
print("Berat Badan:", berat_badan)
print("Alamat:", alamat)

# Operasi aritmatika dengan variabel
tahun_lahir = 2020 - umur
print("Tahun Lahir:", tahun_lahir)
