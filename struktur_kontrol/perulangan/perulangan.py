# Contoh penggunaan perulangan (for loop) dalam Python

# List contoh
buah = ["apel", "pisang", "jeruk", "mangga"]

# Perulangan untuk menampilkan setiap buah
print("Daftar Buah:")
for b in buah:
    print(b)

# Perulangan dengan range
print("\nAngka dari 1 sampai 5:")
for i in range(1, 6):
    print(i)
