# Contoh penggunaan percabangan (if-else) dalam Python

# Deklarasi variabel
nilai = 85

# Percabangan if-else
if nilai >= 80:
    print("Nilai Anda:", nilai)
    print("Selamat! Anda lulus dengan nilai sangat baik.")
elif nilai >= 60:
    print("Nilai Anda:", nilai)
    print("Anda lulus.")
else:
    print("Nilai Anda:", nilai)
    print("Maaf, Anda tidak lulus.")
