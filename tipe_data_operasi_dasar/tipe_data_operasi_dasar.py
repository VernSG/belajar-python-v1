# Contoh penggunaan tipe data dan operasi dasar dalam Python

# Tipe data: integer dan float
umur = 25
tinggi = 175.5

# Operasi aritmatika
hasil_tambah = umur + 5
hasil_kali = umur * tinggi
hasil_bagi = tinggi / 2

print("Umur:", umur)
print("Tinggi:", tinggi)
print("Umur + 5 adalah", hasil_tambah)
print("Umur dikali tinggi adalah", hasil_kali)
print("Tinggi dibagi 2 adalah", hasil_bagi)

# Tipe data: string
nama = "John Doe"
umur_str = str(umur)  # Mengubah integer umur menjadi string

# Operasi pada string
panjang_nama = len(nama)
nama_depan = nama.split()[0]

print("\nNama:", nama)
print("Panjang nama:", panjang_nama)
print("Nama depan:", nama_depan)
